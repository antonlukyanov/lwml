// Вывод лога операций по распределению памяти.
// Использование этой опции существенно увеличивает объем лога
// и замедляет работу большинства приложений.

#define _LOG_MEM_ 0

// Управление проверкой диапазона индексов.
// Ненулевое значение _CHECK_IDX_ гарантирует проверку диапазона индексов
// в опереациях индексирования в простых контейнерных классах.
// В рабочей версии рекомендуется определять константу
// _CHECK_IDX_ значением 0 для ускорения работы.

#define _CHECK_IDX_ 1

// Настройки кодировок файловой системы и консоли.

