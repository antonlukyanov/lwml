// Трактовка знакового сдвига вправо как арифметического сдвига.
// Если эта константа определена как 1, то сдвиг вправо знакового
// числа считается арифметическим.
// В противном случае арифметический сдвиг моделируется через логический.

#define _SIGN_SHR_IS_ARITHM_ 1

// Использование системно-зависимых функций win32 API.
